document.addEventListener('DOMContentLoaded', function () {
  // 「#」だけのダミーリンクは除外する
  const jsSmoothScroll = document.querySelectorAll('a[href^="#"]:not([href="#"])');

  jsSmoothScroll.forEach(function (link) {
    link.addEventListener('click', function (e) {
      const href = this.getAttribute('href'); // 例: "#page-top"
      const id = href.slice(1);              // 例: "page-top"
      const target = document.getElementById(id);

      // 飛び先が無いリンクはデフォルト動作に任せる
      if (!target) return;

      e.preventDefault();

      const rect = target.getBoundingClientRect().top;
      const offset = window.pageYOffset;     // 現在のスクロール量
      const position = rect + offset;        // ページ全体でのY座標

      window.scrollTo({
        top: position,
        behavior: 'smooth'
      });
    });
  });
});
